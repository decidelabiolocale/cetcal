    <p>
      L'association Castillonnais en transition à travers le tiers-lieu «  de-ci de-là » développe 4 activités : une épicerie bio participative, un café associatif qui propose des animations, conférences et actions sur des sujets écologiques (construire en terre, permaculture, compostage...), « la forge enchantée » à St Michel de Montaigne qui propose notamment des formations et le développement des « Circuits alimentaires locaux » avec notamment l’accompagnement de restaurants scolaires vers le bio et la mise en place d'une plateforme numérique qui vise là recenser 'exhaustivité des producteurs bio et de leurs lieux de commercialisation dans un rayon de 40 km autour de Castillon, plateforme en construction mais accessible https://www.cetcal.site/# Pour en savoir plus sur CET, notre rapport d'activité 2020 est en PJ ainsi qu'un bulletin d'adhésion ;-)
    </p>
    <p>
      Pour ce qui concerne la bio notre objectif est de participer activement à la reconstitution d'une ceinture alimentaire bio autour de l'agglo bordelaise qui déborderait sur les départements périphériques. Localement nous travaillons au développement de la production et de la consommation de la bio locale, avec le double objectif de rémunérer correctement les producteurs, tout en ayant un prix abordable pour le plus grand nombre. Notre marge sur les produits locaux est de 5%.
    </p>
    <p>
      Pour cela nous avons mis en place un groupement d'achat bio qui a évolué vers une micro épicerie depuis que nous disposons d'un local à Castillon. Cela nous a permis de compléter l'offre locale avec les produits de négociants margés à 15%. En 2020 nous avons travaillé avec 37 producteurs et transformateurs locaux bio générant un chiffre d'affaire de 65 000 €. Ce qui nous pose un problème fiscal, l'administration considère que nous ne sommes plus « à but non lucratif » et que si nous poursuivons dans cette voie nous devons nous fiscaliser.
    </p>
    <p>
      Une subvention de la région nous avait permis de rémunérer une salariée à mi-temps en 2020. Depuis janvier 2021 le travail repose uniquement sur une quinzaine de bénévoles avec une charge de travail que nous n'arriverons pas à assumer dans le temps. Nous allons devoir évoluer et travaillons sur 3 hypothèses :
    </p>
